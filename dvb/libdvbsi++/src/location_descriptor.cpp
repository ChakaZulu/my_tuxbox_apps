#include <dvbsi++/location_descriptor.h>

LocationDescriptor::LocationDescriptor(const uint8_t * const buffer) : Descriptor(buffer)
{
	locationTag = buffer[2];
}

uint8_t LocationDescriptor::getLocationTag(void) const
{
	return locationTag;
}
